#include "easing.h"

//=========================================================
// 僀乕僕儞僌偺僙僢僞乕
//=========================================================
float CEasing::SetEase(int EaseFrame, int MaxEaseFrame)
{
	// 僼儗乕儉偑嵟戝傑偱峴偭偨傜巭傔傞
	if (EaseFrame >= MaxEaseFrame)
	{
		EaseFrame = MaxEaseFrame;
	}

	// t傪媮傔傞
	float t = static_cast<float>(EaseFrame) / static_cast<float>(MaxEaseFrame);

	// t偺抣傪曉偡
	return t;
}

//=========================================================
// 僀乕僕儞僌偺娚傗偐側壛懍
//=========================================================
float CEasing::EaseInsine(float t)
{
	return 1.0f - cos((t * D3DX_PI) * 0.5f);
}
//=========================================================
// 僀乕僕儞僌偺娚傗偐側尭懍
//=========================================================
float CEasing::EaseOutsine(float t)
{
	return sin((t * D3DX_PI) * 0.5f);
}
//=============================================================
// 娚傗偐側壛懍仺尭懍
//=============================================================
float CEasing::EaseInOutSine(float t)
{
	return -(cos(D3DX_PI * t) - 1.0f) * 0.5f;
}

//=============================================================
// 娚傗偐仺壛懍
//=============================================================
float CEasing::EaseInQuad(float t)
{
	return t * t;
}
//=============================================================
// 壛懍仺尭懍
//=============================================================
float CEasing::EaseOutQuad(float t)
{
	return 1.0f - (1.0f - t) * (1.0f - t);
}
//================================================================
// 備偭偔傝壛懍仺尭懍偟偰備偭偔傝巭傑傞
//================================================================
float CEasing::EaseInOutQuad(float t)
{
	return static_cast<float>(t < 0.5f ? 2.0f * t * t : 1.0f - pow(-2.0f * t + 2.0f, 2.0f) * 0.5f);
}

//=============================================================
// EaseInQuad傛傝傕備偭偔傝奐巒
//=============================================================
float CEasing::EaseInCubic(float t)
{
	return t * t * t;
}
//=============================================================
// 嵟弶壛懍仺尭懍偟偰巭傑傞
//=============================================================
float CEasing::EaseOutCubic(float t)
{
	return 1.0f - pow(1.0f - t, 3.0f);
}
//=============================================================
// 備偭偔傝奐巒仺懍偔摦偄偰偐傜尭懍
//=============================================================
float CEasing::EaseInOutCubic(float t)
{
	return static_cast<float>(t < 0.5f ? 4.0f * t * t * t : 1.0f - pow(-2.0f * t + 2.0f, 3.0f) * 0.5f);
}

//=============================================================
// 僶僂儞僪偟偰掆巭
//=============================================================
float CEasing::EaseBounce(float t)
{
	if (t < (1.0f / 2.75f))  // 嵟弶偺僶僂儞僪
	{
		return 20.0f * t * t;  // 嫮偝傪挷惍偟偰丄僶僂儞僪傪嫮挷
	}
	else if (t < (2.0f / 2.75f))  // 2斣栚偺僶僂儞僪
	{
		t -= (1.5f / 2.75f);
		return 20.0f * t * t + 0.75f;  // 嫮偝傪挷惍偟偰丄僶僂儞僪傪嫮挷
	}
	else if (t < (2.5f / 2.75f))  // 3斣栚偺僶僂儞僪
	{
		t -= (2.25f / 2.75f);
		return 20.0f * t * t + 0.9375f;  // 嫮偝傪挷惍偟偰丄僶僂儞僪傪嫮挷
	}
	else  // 嵟屻偺僶僂儞僪
	{
		t -= (2.625f / 2.75f);
		return 20.0f * t * t + 0.984375f;  // 嫮偝傪挷惍偟偰丄僶僂儞僪傪嫮挷
	}
}

//=============================================================
// 嵟弶懍偄仺妸傜偐偵巭傑傞
//=============================================================
float CEasing::EaseOutQuart(float t)
{
	return 1.0f - pow(1.0f - t, 4.0f);
}
//=============================================================
// 備偭偔傝奐巒仺媫尭懍偟偰掆巭
//=============================================================
float CEasing::EaseInOutQuart(float t)
{
	return t < 0.5f ? 8.0f * t * t * t * t : 1.0f - pow(-2.0f * t + 2.0f, 4.0f) * 0.5f;
}

//=============================================================
// 堦婥偵恑傒媫掆巭
//=============================================================
float CEasing::EaseOutQuint(float t)
{
	return 1.0f - pow(1.0f - t, 5.0f);
}
//=============================================================
// 僗儘乕僗僞乕僩仺嵟屻偵媫掆巭
//=============================================================
float CEasing::EaseInOutQuint(float t)
{
	return static_cast<float>(t < 0.5f ? 16.0f * t * t * t * t * t : 1.0f - pow(-2.0f * t + 2.0f, 5.0f) * 0.5f);
}

//=============================================================
// 堦搙媡曽岦仺壛懍偡傞
//=============================================================
float CEasing::EaseInBack(float t)
{
	const float s = 0.70158f;  // 堷偭挘傝搙崌偄
	return t * t * ((s + 1.0f) * t - s);  // 斀揮偺摦偒
}
//=============================================================
// 堦搙捠傝夁偓傞仺栠偭偰掆巭
//=============================================================
float CEasing::EaseOutBack(float t)
{
	const float s = 0.70158f;  // 堷偭挘傝搙崌偄
	t -= 1.0f;
	return (t * t * ((s + 1.0f) * t + s) + 1.0f);  // 斀揮偺摦偒
}
//===============================================================
// 堦搙媡曽岦仺堦搙捠傝夁偓偰偐傜掆巭
//===============================================================
float CEasing::EaseInOutBack(float t)
{
	const float s = 0.70158f * 1.525f;  // 堷偭挘傝搙崌偄偺挷惍

	if (t < 0.5f)
	{
		t *= 2.0f;
		return 0.5f * (t * t * ((s + 1.0f) * t - s));  // 慜敿偼斀揮偲壛懍
	}
	else
	{
		t = t * 2.0f - 2.0f;
		return 0.5f * (t * t * ((s + 1.0f) * t + s) + 2.0f);  // 屻敿偼斀揮偲尭懍
	}
}
//=============================================================
// 堦搙媡曽岦偵堷偭挘傞仺怳摦偱恑傓
//=============================================================
float CEasing::EaseInElastic(float t)
{
	if (t == 0.0f)
	{
		return 0.0f;  // 奐巒帪揰
	}
	if (t == 1.0f)
	{
		return 1.0f;  // 廔椆帪揰
	}
	const float p = 0.3f;  // 怳摦廃婜
	const float s = p * 0.25f;  // 怳摦偺嫮偝

	return static_cast<float>(-(pow(2.0f, 10.0f* (t -= 1.0f)) * sin((t - s) * (2.0f * D3DX_PI) / p)));  // 怳摦偺摦偒
}
//=============================================================
// 怳摦偟側偑傜巭傑傞
//=========================================================
float CEasing::EaseOutElastic(float t)
{
	if (t == 0.0f)
	{
		return 0.0f;  // 奐巒帪揰
	}
	if (t == 1.0f)
	{
		return 1.0f;  // 廔椆帪揰
	}

	const float p = 0.3f;  // 怳摦廃婜
	const float s = p * 0.25f;  // 怳摦偺嫮偝

	return static_cast<float>(pow(2.0f, -10.0f * t) * sin((t - s) * (2.0f * D3DX_PI) / p) + 1.0f);  // 怳摦偺摦偒

}
//===========================================================
// 奐巒偱怳摦仺廔椆帪怳摦偟偰偐傜掆巭
//===========================================================
float CEasing::EaseInOutElastic(float t)
{
	if (t == 0.0f)
	{
		return 0.0f;  // 奐巒帪揰
	}
	if (t == 1.0f)
	{
		return 1.0f;  // 廔椆帪揰
	}
	const float p = 0.3f * 1.5f;  // 怳摦廃婜
	const float s = p * 0.25f;  // 怳摦偺嫮偝

	if (t < 0.5f)
	{
		return static_cast<float>(-0.5f * (pow(2.0f, 20.0f * t - 10.0f) * sin((20.0f * t - 11.125f) * (2.0f * D3DX_PI) / p)));
	}

	return static_cast<float>(pow(2.0f, -20.0f * t + 10.0f) * sin((20.0f * t - 11.125f) * (2 * D3DX_PI) / p) * 0.5f + 1.0f);
}